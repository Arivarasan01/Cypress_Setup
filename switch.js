// switch
let grade = "Z";

switch(grade){
    case "S":
        console.log("Super Grade");
        break;
    case "A":
    case "B":    
        console.log ("Excellent Grade");
        break;
    case "E":
        console.log ("Just Pass");
        break;
    case "U":
        console.log ("Failed");
        break;    
    default:
        console.log ("Unknown Grade");
            
}